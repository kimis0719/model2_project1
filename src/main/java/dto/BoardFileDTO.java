package dto;

public class BoardFileDTO {
	private int file_num;
	private String file_oName;
	private String file_fName;
	private String file_yn;
	private int board_num;
	
	public int getFile_num() {
		return file_num;
	}
	public void setFile_num(int file_num) {
		this.file_num = file_num;
	}
	public String getFile_oName() {
		return file_oName;
	}
	public void setFile_oName(String file_oName) {
		this.file_oName = file_oName;
	}
	public String getFile_fName() {
		return file_fName;
	}
	public void setFile_fName(String file_fName) {
		this.file_fName = file_fName;
	}
	public String getFile_yn() {
		return file_yn;
	}
	public void setFile_yn(String file_yn) {
		this.file_yn = file_yn;
	}
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
}
